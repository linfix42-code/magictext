function refresh() {
    const text = input.value || "Cool Nick";
    const style = selector.value;
    const isStylePremium = style.startsWith('premium');

    // Трансформація
    const transformed = text.split('').map(char => (maps[style] && maps[style][char]) ? maps[style][char] : char).join('');
    display.textContent = transformed;

    // Логіка кнопки копіювання
    if (isStylePremium && !isPremiumActive()) {
        copyBtn.disabled = true;
        copyBtn.textContent = "🔒 Тільки у Premium";
    } else {
        copyBtn.disabled = false;
        copyBtn.textContent = "Скопіювати для соцмереж";
    }

    // Додаткові шрифти Google Fonts для преміуму
    if (style === 'premium_script') display.style.fontFamily = "'Dancing Script', cursive";
    else display.style.fontFamily = "'Roboto', sans-serif";
}

function copyText() {
    if (copyBtn.disabled) return;
    navigator.clipboard.writeText(display.textContent).then(() => {
        alert("Текст успішно скопійовано!");
    });
}

// Режим розробника
document.querySelector('h2').addEventListener('click', () => {
    window.clicks = (window.clicks || 0) + 1;
    if (window.clicks === 5) {
        const state = isPremiumActive();
        localStorage.setItem('premiumActive', !state);
        alert(!state ? "👑 Premium Активовано!" : "Premium вимкнено");
        location.reload();
    }
});

input.addEventListener('input', refresh);
selector.addEventListener('change', refresh);
window.onload = refresh;
